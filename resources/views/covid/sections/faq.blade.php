                        <div class="f_tab" id="f_tab_6">
                            <div class="faq_content_box">
                                <h2>Érdekességek a SARS-CoV-2 vírusról</h2>
                                <p>Kínai kutatók szerint a vírusok folyamatosan mutálódnak, de a legtöbb változás csekély hatással van a vírus viselkedésére. Más mutációk megváltoztathatják a biológiai tulajdonságokat, lehetővé téve számukra, hogy alkalmazkodjanak a különböző környezethez. A vírusfejlődés valószínűleg megtörtént a különböző személyek közötti átadás során. Február 19-én azonban a hatóságok azt jelentették, hogy a vírus legfrissebb megfigyelése alapján nem találtak mutációt, mert a különféle koronavírusminták genomszekvenciája majdnem azonos.</p>
                                <p>Március elején a Pekingi Egyetem és a sanghaji Pasteur Intézet kutatói a koronavírusról azt állapították meg, hogy a járvány kitörése alatt a SARS-CoV-2 két vírustörzsre vált szét, és az egyik törzs sokkal agresszívebb a másiknál.</p>
                                <p>Március 5-én járványügyi kutatók szerint a SARS-CoV-2, a COVID–19 betegséget okozó koronavírus alap szaporodási ráta (R0) értéke körülbelül 2,2-re becsülhető, vagyis egyetlen fertőzött személy átlagosan körülbelül 2,2 másik személyt fertőz meg. Összehasonlításképpen: az influenza R0 értéke 1,3. Mindaddig, amíg az R0 értéke 1,0 alá nem csökken, a járvány tovább terjed, ellenkező esetben a járvány hosszú távon elhal. A tapasztalatok szerint a teszteléssel történő tömeges szűrés és a szociális elkülönítése lassíthatja a járvány terjedését.</p>
                                <p>A WHO március 17-én kiadott jelentése szerint, a bizonyított betegek esetei alapján, a betegség mintegy 3–4 százaléka végződik halállal, ami jóval magasabb, mint a szezonális influenza 1 százalék alatti halálozási rátája. Az időskorúak és a más betegségben is szenvedők a legveszélyeztetettebbek.</p>
                                <p>Március 23-án Genfben a FIFA és az Egészségügyi Világszervezet képviselői összefogtak a COVID–19 betegség és a pandémia leküzdésére, ezért egy új figyelemfelhívó kampányt indítottak, amelyet világhírű labdarúgók vezetnek, akik felszólítják a világon élő embereket, hogy tartsanak be öt kulcsfontosságú szabályt cseppfertőzés és a vírus terjedése ellen:</p>
                                <ul>
                                    <li>-kézmosás</li>
                                    <li>-köhögési etikett</li>
                                    <li>-az arcot nem szabad megérinteni</li>
                                    <li>-az egymástól való kellő fizikai távolságot be kell tartani</li>
                                    <li>-aki rosszul érzi magát az maradjon otthon</li>
                                </ul>
                                <p class="mt-3">Az egészségügyi védőfelszerelések beszerzése illetve előállítása lett a prioritás világszerte. Ugyanakkor egyre több kezelési és megelőzési útmutató lát napvilágot. Több ország általánosan szorgalmazza, vagy épp előírja az egészségügyi maszkok viselését is közterületen. A WHO azonban nem ajánlja azzal az indokkal, hogy a nem megfelelő használata hamis védelmet sugall.</p>
                                <p>A világ számos országában szünetel az oktatás. Legelőször a csoportos rendezvények megtartását tiltották be. Fokozatosan kerültek bevezetésre a rendkívüli állapotok, országonként eltérő szabályozással. Határátlépési és utazási korlátozások vannak érvényben, aminek következtében számos gazdasági ágazat takaréklángon működik, vagy épp szünetelteti tevékenységét (légiközlekedés, turizmus, autógyártás, stb.).</p>
                                <p>A "Maradj otthon" mottó alatt az utcák kiüresedtek szerte a világon, ellenben egyre több szakvélemény lát napvilágot arról, hogy a szigorú intézkedések súlyosabb kárt okozhatnak a lakosságnak, mint ha egyáltalán nem vezették volna be. Jónéhány elismert tudós pedig amellett foglal állást, hogy eltúlzott az a figyelem, ami a koronavírust követi.</p>
                                <p>Világviszonylati összegzésben is csökkenő tendenciát mutat a járvány terjedése április második felére. Ennek következtében a kormányok egyre hangsúlyosabban kezdtek dolgozni a gazdaság újraindításának tervein és átlagban május közepétől számítják feloldani a kijárási korlátozásokat. Mindeközben továbbra is fokozott erőfeszítéssel szerelik fel az egészségügyi intézményeket járványügyi megelőző eszközökkel.</p>
